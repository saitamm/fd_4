#include "../include_file/fdf.h"

int	ft_help_z(int z, int max, int min)
{
	if (max <= 10 && min >= 0)
		z *= 25;
	else if (max > 10 && max <= 20 && min > 0)
		z *= 25;
	else
		z *= 1;
	return (z);
}
